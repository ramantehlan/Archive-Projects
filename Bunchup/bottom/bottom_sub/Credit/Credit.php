
<h2>Credit</h2>
<p>  Bunchup was started with a aim to provide mix of all the social networking services in one site. Bunchup idea came from a <b>15 year old boy</b>, who wanted to sign up only at one site to get all 
	different social services. So he did a experiment by creating a company as bunchup.inc and launching a site as bunchup.com but this experiment became a beautiful masterpiece. Although site
	have many basic services which can be found in other sites too but thing which make bunchup different is mix of many social services. Bunchup don't want its users to follow old trend of social
	networking. Bunchup credit goes to that boy and his <b>team of professional</b> who is working day and night to make you close to your friends.

